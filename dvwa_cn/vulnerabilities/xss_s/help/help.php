	<td><div id="code">

		<p>跨站攻击，即Cross Site Script Execution(通常简写为XSS)是指攻击者利用网站程序对用户输入过滤不足，输入可以显示在页面上对其他用户造成影响的HTML代码，从而盗取用户资料、利用用户身份进行某种动作或者对访问者进行病毒侵害的一种攻击方式。</P>

		<p>存储型跨站是跨站脚本攻击的一种，存储型跨站危害要比反射的型的大的多，它是存储在数据中，只要用户访问过此网站就会被攻击。</p>
		
		<p>本例中XSS代码将长久有效，直到数据库重置或者是手工从数据库中清除。</p>
		
